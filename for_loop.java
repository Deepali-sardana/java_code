package com.company;

public class for_loop {
    public static void main(String[]args){
        for(int i=0;i<=5;i++)//in for loop we initialize the variable compare it and do update(inc/dec)
        {
            System.out.println(i);
        }
        //example of for loop to print first 10 odd number
        for(int i=0;i<=10;i++){
            System.out.println(2*i+1);
        }
    }
}

