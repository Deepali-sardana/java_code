package com.company;

public class while_loop {
    public static void main(String[]args){
        int i=0;//initialization
        while(i<=10)//condition
        {
            System.out.println(i);
            i++;//update
        }
        //print first 10 even number
        int j=0;
        while (j<10){
            System.out.println(2*j);
            j++;
        }
    }
}
